// declaramos variables
const numero1 = 10;
const numero2 = 15;
// creamos funcion
function sumar(a, b) {
    // return 
    /*
        es una forma de que nuestra funcion
        retorne un valor que puede ser almacenado
        y utilizado
    */
    return a + b;
};

// invocamos la funcion y almacenamos el retorno en una variable
const resultado = sumar(numero1, numero2);

// mostramos el resultado en consola
console.log(resultado);

